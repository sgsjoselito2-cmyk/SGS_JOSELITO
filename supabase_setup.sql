-- Supabase Setup SQL
-- This script creates all necessary tables for the Joselito App.
-- Security (RLS) is disabled for all tables as requested.

-- 1. Activities Table
CREATE TABLE IF NOT EXISTS activities (
    id TEXT PRIMARY KEY,
    operario TEXT NOT NULL,
    area TEXT NOT NULL,
    formato TEXT NOT NULL,
    inicio TIMESTAMP WITH TIME ZONE NOT NULL,
    fin TIMESTAMP WITH TIME ZONE,
    fecha DATE,
    cantidad INTEGER DEFAULT 0,
    tiempoTeorico INTEGER DEFAULT 0,
    isIncidencia BOOLEAN DEFAULT FALSE,
    tipoIncidencia TEXT,
    afectaCalidad BOOLEAN DEFAULT FALSE,
    requiereMaquina BOOLEAN DEFAULT FALSE,
    maquina TEXT,
    piezasNOK INTEGER DEFAULT 0,
    observaciones TEXT,
    lastModified TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 2. History Table
CREATE TABLE IF NOT EXISTS history (
    id TEXT PRIMARY KEY,
    operario TEXT NOT NULL,
    area TEXT NOT NULL,
    formato TEXT NOT NULL,
    inicio TIMESTAMP WITH TIME ZONE NOT NULL,
    fin TIMESTAMP WITH TIME ZONE NOT NULL,
    fecha DATE NOT NULL,
    cantidad INTEGER DEFAULT 0,
    tiempoTeorico INTEGER DEFAULT 0,
    isIncidencia BOOLEAN DEFAULT FALSE,
    tipoIncidencia TEXT,
    afectaCalidad BOOLEAN DEFAULT FALSE,
    requiereMaquina BOOLEAN DEFAULT FALSE,
    maquina TEXT,
    piezasNOK INTEGER DEFAULT 0,
    observaciones TEXT,
    lastModified TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 3. Operarios Table
CREATE TABLE IF NOT EXISTS operarios (
    id TEXT PRIMARY KEY,
    nombre TEXT NOT NULL,
    area TEXT,
    areas TEXT[],
    lastModified TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 4. OEE Objectives Table
CREATE TABLE IF NOT EXISTS oee_objectives (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    area TEXT NOT NULL,
    disponibilidad NUMERIC NOT NULL,
    rendimiento NUMERIC NOT NULL,
    calidad NUMERIC NOT NULL,
    productividad NUMERIC NOT NULL,
    objetivo NUMERIC DEFAULT 0,
    validFrom DATE NOT NULL,
    lastModified TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 5. TOP60 Seguridad Table
CREATE TABLE IF NOT EXISTS top60_seguridad (
    id BIGINT PRIMARY KEY,
    fecha DATE,
    asunto TEXT,
    accion TEXT,
    responsable TEXT,
    soporte TEXT,
    fechaLanzamiento DATE,
    fechaObjetivo DATE,
    avance INTEGER,
    observaciones TEXT,
    lastModified TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 6. TOP60 RRHH Table
CREATE TABLE IF NOT EXISTS top60_rrhh (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    fecha DATE NOT NULL,
    area TEXT NOT NULL,
    absentismo NUMERIC DEFAULT 0,
    ausentismo NUMERIC DEFAULT 0,
    lastModified TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 7. TOP60 Ausentismo Table
CREATE TABLE IF NOT EXISTS top60_ausentismo (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    fecha DATE NOT NULL,
    area TEXT NOT NULL,
    valor NUMERIC DEFAULT 0,
    lastModified TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 8. TOP60 Calidad Table
CREATE TABLE IF NOT EXISTS top60_calidad (
    id TEXT PRIMARY KEY,
    semana INTEGER NOT NULL,
    anio INTEGER NOT NULL,
    imagenes TEXT[], -- Array of base64 strings
    fechaCreacion TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    lastModified TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 9. TOP60 IdM Table (Ideas de Mejora)
CREATE TABLE IF NOT EXISTS top60_idm (
    id TEXT PRIMARY KEY,
    idSugerencia INTEGER,
    asunto TEXT,
    accion TEXT,
    responsable TEXT,
    soporte TEXT,
    fechaLanzamiento DATE,
