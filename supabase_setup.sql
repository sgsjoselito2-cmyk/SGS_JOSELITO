-- 10. Master Speeds Table
CREATE TABLE IF NOT EXISTS master_speeds (
    id TEXT PRIMARY KEY,
    area TEXT NOT NULL,
    formato TEXT NOT NULL,
    tiempoTeorico INTEGER NOT NULL,
    lastModified TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Migration: Rename columns if they exist (PostgreSQL doesn't have RENAME COLUMN IF EXISTS directly in one line)
DO $$ 
BEGIN 
    IF EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='master_speeds' AND column_name='tiempo_teorico') THEN
        ALTER TABLE master_speeds RENAME COLUMN tiempo_teorico TO tiempoTeorico;
    END IF;
    
    IF EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='activities' AND column_name='tiempo_teorico') THEN
        ALTER TABLE activities RENAME COLUMN tiempo_teorico TO tiempoTeorico;
    END IF;

    IF EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='history' AND column_name='tiempo_teorico') THEN
        ALTER TABLE history RENAME COLUMN tiempo_teorico TO tiempoTeorico;
    END IF;
    
    IF EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='incidence_master' AND column_name='afecta_calidad') THEN
        ALTER TABLE incidence_master RENAME COLUMN afecta_calidad TO afectaCalidad;
    END IF;
    
    IF EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='incidence_master' AND column_name='requiere_maquina') THEN
        ALTER TABLE incidence_master RENAME COLUMN requiere_maquina TO requiereMaquina;
    END IF;

    IF EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='oee_objectives' AND column_name='valid_from') THEN
        ALTER TABLE oee_objectives RENAME COLUMN valid_from TO validFrom;
    END IF;

    IF EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='top60_seguridad' AND column_name='fecha_lanzamiento') THEN
        ALTER TABLE top60_seguridad RENAME COLUMN fecha_lanzamiento TO fechaLanzamiento;
    END IF;

    IF EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='top60_seguridad' AND column_name='fecha_objetivo') THEN
        ALTER TABLE top60_seguridad RENAME COLUMN fecha_objetivo TO fechaObjetivo;
    END IF;
END $$;

